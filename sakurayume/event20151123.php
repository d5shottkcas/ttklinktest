<div class="container context-center mb-3">
Dream5が2015年11月4日にデビュー6周年を迎え、11月23日に6周年記念ワンマンライブが行われました。<br>
<br>
チケットは販売直後に完売したそうで、開演直前の会場付近はとても賑わっていました。<br> 
開演に先立ち、13時30分からはグッズなどの販売も行われ、今回のワンマンライブ限定のグッズなども多数販売され、<br>
朝から寒い中たくさんの方が並んでいました。<br> 
<br>
今回のライブでは、「お祭り」がテーマとなっており、グッズのデザインやステージ、セットリストも「お祭り」を意識したものとなっていました。<br>
特にステージは、神社のお祭舞台を意識したものとなっていて、提灯風の照明などでお祭り感がとても出ていました。<br>
<br>
ライブ序盤では「お祭り」に合った曲として、JITTERIN' JINNさんの「夏祭り」、氣志團さんの「ONE NIGHT CARNIVAL」、<br>
そしていきものがかりさんの「じょいふる」をカバーしました。
<br>
ライブ中盤ではヒット曲「ようかい体操第一」などを披露した後に、ダンサー三人による「ダンスショーケース」が披露され、<br>
その次にボーカル二人による「Go my way!」が披露されました。<br>
<br>
その後、2月16日にリリースが決定した新ミニアルバムに、小室哲哉さんから楽曲提供されることが発表され、<br>
同ミニアルバムに収録されることが決まっているTRFさんのカバー曲「BOY MEETS GIRL」が披露されました。<br>
アンコールでは、今年のツアー限定曲として発表され、新ミニアルバムへの収録が発表された「カラフルチューン」を披露。<br>
間奏ではメンバーがステージから客席へボールを投げるパフォーマンスもありました。<br>
そして最後にメンバーから挨拶があり、エンディングムービーが流れてフィニッシュとなりました。<br>
